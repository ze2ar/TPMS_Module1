def poisson_formatter(n: int, p: float, m: int = None) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –æ—Ç–≤–µ—Ç –ø–æ —Ç–µ–æ—Ä–µ–º–µ –ü—É–∞—Å—Å–æ–Ω–∞.

    üìù –§–æ—Ä–º–∞—Ç—ã:
    - –î–ª—è P(X = m): <a>^<m>/<m>!e^-<a>
    - –î–ª—è P(X >= 1): 1 - e^-<a>

    –ü—Ä–∏–º–µ—Ä—ã —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞:
    2,57^2/2!e^-2,57
    1 - e^-2,57

    :param n: –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏—Å–ø—ã—Ç–∞–Ω–∏–π
    :param p: –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —É—Å–ø–µ—Ö–∞ –≤ –æ–¥–Ω–æ–º –∏—Å–ø—ã—Ç–∞–Ω–∏–∏
    :param m: –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–∞—Å—Ç—É–ø–∏–≤—à–∏—Ö —Å–æ–±—ã—Ç–∏–π (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ, –µ—Å–ª–∏ None, —Ç–æ P(X >= 1))
    :return: –§–æ—Ä–º—É–ª–∞ –ü—É–∞—Å—Å–æ–Ω–∞ —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
    """
    a = n * p
    a_str = format_number(a)

    if m is not None:
        m_str = str(m)
        result = f"{a_str}^{m_str}/{m_str}!e^-{a_str}"
    else:
        result = f"1 - e^-{a_str}"

    return result


def local_moivre_laplace_formatter(n: int, p: float, x_target: int) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –æ—Ç–≤–µ—Ç –ø–æ –ª–æ–∫–∞–ª—å–Ω–æ–π —Ç–µ–æ—Ä–µ–º–µ –ú—É–∞–≤—Ä–∞-–õ–∞–ø–ª–∞—Å–∞ –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏ –¥–ª–∏–Ω–æ–π 20 —Å–∏–º–≤–æ–ª–æ–≤.

    üìù –§–æ—Ä–º–∞—Ç: 1/<œÉ>fi(<|x_m|>)
    –ü—Ä–∏–º–µ—Ä: 1/2,57fi(3,48)

    :param n: –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏—Å–ø—ã—Ç–∞–Ω–∏–π
    :param p: –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —É—Å–ø–µ—Ö–∞ –≤ –æ–¥–Ω–æ–º –∏—Å–ø—ã—Ç–∞–Ω–∏–∏
    :param x_target: –¶–µ–ª–µ–≤–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —É—Å–ø–µ—Ö–æ–≤
    :return: –§–æ—Ä–º—É–ª–∞ –ª–æ–∫–∞–ª—å–Ω–æ–π —Ç–µ–æ—Ä–µ–º—ã –ú—É–∞–≤—Ä–∞-–õ–∞–ø–ª–∞—Å–∞ —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
    """
    mu = n * p
    sigma = math.sqrt(n * p * (1 - p))
    x_m = (x_target - mu) / sigma

    sigma_str = format_number(sigma)
    x_m_abs_str = format_number(abs(x_m))

    result = f"1/{sigma_str}fi({x_m_abs_str})"
    return result


def integral_moivre_laplace_formatter(n: int, p: float, x1: int, x2: int = None) -> str:
    """
    –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç –æ—Ç–≤–µ—Ç –ø–æ –∏–Ω—Ç–µ–≥—Ä–∞–ª—å–Ω–æ–π —Ç–µ–æ—Ä–µ–º–µ –ú—É–∞–≤—Ä–∞-–õ–∞–ø–ª–∞—Å–∞ –≤ –≤–∏–¥–µ —Å—Ç—Ä–æ–∫–∏.

    üìù –§–æ—Ä–º–∞—Ç—ã:
    - –î–ª—è P(x1 <= X <= x2): –§_0(|a|) ¬± –§_0(|b|)
    - –î–ª—è P(X >= x1): 1 - –§_0(|a|) –∏–ª–∏ 1 + –§_0(|a|)

    –ü—Ä–∏–º–µ—Ä—ã —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞:
    –§_0(3,48)+–§_0(3,48)
    -–§_0(3,9)+–§_0(4)
    1 - –§_0(2,63)

    :param n: –û–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –∏—Å–ø—ã—Ç–∞–Ω–∏–π
    :param p: –í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —É—Å–ø–µ—Ö–∞ –≤ –æ–¥–Ω–æ–º –∏—Å–ø—ã—Ç–∞–Ω–∏–∏
    :param x1: –õ–µ–≤–∞—è –≥—Ä–∞–Ω–∏—Ü–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —É—Å–ø–µ—Ö–æ–≤
    :param x2: –ü—Ä–∞–≤–∞—è –≥—Ä–∞–Ω–∏—Ü–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ —É—Å–ø–µ—Ö–æ–≤ (–æ–ø—Ü–∏–æ–Ω–∞–ª—å–Ω–æ, –µ—Å–ª–∏ None, —Ç–æ P(X >= x1))
    :return: –§–æ—Ä–º—É–ª–∞ –∏–Ω—Ç–µ–≥—Ä–∞–ª—å–Ω–æ–π —Ç–µ–æ—Ä–µ–º—ã –ú—É–∞–≤—Ä–∞-–õ–∞–ø–ª–∞—Å–∞ —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–∞–º–∏
    """
    mu = n * p
    sigma = math.sqrt(n * p * (1 - p))

    # –ù–æ—Ä–º–∞–ª–∏–∑–∞—Ü–∏—è –≥—Ä–∞–Ω–∏—Ü
    a = (x1 - mu) / sigma
    if x2 is not None:
        b = (x2 - mu) / sigma
    else:
        b = float("inf")  # –§–æ—Ä–º–∞–ª—å–Ω–æ b -> +‚àû

    a_str = format_number(abs(a))
    b_str = format_number(abs(b)) if b != float("inf") else None

    if x2 is None:
        # –°–ª—É—á–∞–π P(X >= x1)
        result = f"1 - –§_0({a_str})" if a >= 0 else f"1 + –§_0({a_str})"
    else:
        # –°–ª—É—á–∞–π P(x1 <= X <= x2)
        if a < 0 and b < 0:
            result = f"-–§_0({b_str})+–§_0({a_str})"
        elif a >= 0 and b >= 0:
            result = f"–§_0({b_str})-–§_0({a_str})"
        else:
            result = f"–§_0({b_str})+–§_0({a_str})"

    return result


def format_number(x: float) -> str:
    """
    –§–æ—Ä–º–∞—Ç—Ç–µ—Ä –¥–ª—è —á–∏—Å–µ–ª —Å–æ–≥–ª–∞—Å–Ω–æ –ø—Ä–∞–≤–∏–ª–∞–º

    :param x: –ò—Å—Ö–æ–¥–Ω–æ–µ —á–∏—Å–ª–æ
    :return: –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–Ω–æ–µ —á–∏—Å–ª–æ
    """
    x = round(float(x), 2)
    return str(x).replace(".", ",").rstrip(",0")
